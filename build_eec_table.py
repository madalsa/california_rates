def midas_to_hourly(block, year=2025):
    """Convert MIDAS 8am-offset data to calendar-hour dict keyed by (date, hour)."""
    lookup = {}
    for r in block:
        date_val = r[2]  # DateStart
        time_val = r[3]  # TimeStart
        value = r[9]
        if hasattr(date_val, 'date'):
            d = date_val.date()
        else:
            from datetime import datetime as dt2
            d = dt2.strptime(str(date_val), '%Y-%m-%d').date() if '-' in str(date_val) else dt2.strptime(str(date_val), '%m/%d/%y').date()
        if hasattr(time_val, 'hour'):
            h = time_val.hour
        else:
            h = int(str(time_val).split(':')[0])
        lookup[(d, h)] = value
    return lookup

